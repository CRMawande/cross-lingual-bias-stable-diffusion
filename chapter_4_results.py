"""**2. Chi-Square Tests of Independence**"""

crosslingual_chi = []

for occ in sorted(analysis_df['occupation'].unique()):

    subset = analysis_df[
        analysis_df['occupation'] == occ
    ]

    contingency = pd.crosstab(
        subset['language'],
        subset['final_label']
    )

    if contingency.shape[1] < 2:

        crosslingual_chi.append({

            'occupation': occ,

            'chi2': "Not Computable",

            'p_value': "Not Computable",

            'degrees_of_freedom': "Not Computable",

            'significant': "No variation"

        })

    else:

        chi2, p, dof, expected = chi2_contingency(
            contingency
        )

        significance = (
            "Yes"
            if p < 0.05
            else "No"
        )

        crosslingual_chi.append({

            'occupation': occ,

            'chi2': round(chi2,2),

            'p_value': round(p,4),

            'degrees_of_freedom': dof,

            'significant': significance

        })

